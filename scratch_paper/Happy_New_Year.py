def lanterns_maker():
    '''制作一对灯笼'''
    
    n,m=0,10
    while m > 0:
        n +=1
        m -=1
        k=f"{'':_^{n*m}}"
        r=f'{k:^60}'
        print(r+r)

for i in range(3):
    lanterns_maker()
